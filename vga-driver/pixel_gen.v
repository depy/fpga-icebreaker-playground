`timescale 1ns / 1ps

module pixel_gen (
    input wire clk,
    input wire reset,
    input wire dataEnable,
    input wire[9:0] x,
    input wire[9:0] y,
    output reg[3:0] R,
    output reg[3:0] G,
    output reg[3:0] B
);

    reg [31:0] smiley [0:31];

    initial begin
        smiley[ 0] = 32'b00000000000000000000000000000000;
        smiley[ 1] = 32'b00000000000111111111100000000000;
        smiley[ 2] = 32'b00000000111100000000111100000000;
        smiley[ 3] = 32'b00000001110000000000001110000000;
        smiley[ 4] = 32'b00000011000000000000000011000000;
        smiley[ 5] = 32'b00000110000000000000000001100000;
        smiley[ 6] = 32'b00001100000000000000000000110000;
        smiley[ 7] = 32'b00011000000000000000000000011000;
        smiley[ 8] = 32'b00110000000000000000000000001100;
        smiley[ 9] = 32'b00110000000000000000000000001100;
        smiley[10] = 32'b00100000011100000000111000000100;
        smiley[11] = 32'b01100000011100000000111000000110;
        smiley[12] = 32'b01000000011100000000111000000010;
        smiley[13] = 32'b01000000000000000000000000000010;
        smiley[14] = 32'b01000000000000000000000000000010;
        smiley[15] = 32'b01000000000000000000000000000010;
        smiley[16] = 32'b01000000000000000000000000000010;
        smiley[17] = 32'b01000000000000000000000000000010;
        smiley[18] = 32'b01000001000000000000000010000010;
        smiley[19] = 32'b01000001100000000000000110000010;
        smiley[20] = 32'b01100000110000000000001100000110;
        smiley[21] = 32'b00100000011100000000111000000100;
        smiley[22] = 32'b00110000000011111111000000001100;
        smiley[23] = 32'b00110000000000000000000000001100;
        smiley[24] = 32'b00011000000000000000000000011000;
        smiley[25] = 32'b00001100000000000000000000110000;
        smiley[26] = 32'b00000110000000000000000001100000;
        smiley[27] = 32'b00000011000000000000000011000000;
        smiley[28] = 32'b00000001110000000000001110000000;
        smiley[29] = 32'b00000000111100000000111100000000;
        smiley[30] = 32'b00000000000111111111100000000000;
        smiley[31] = 32'b00000000000000000000000000000000;
    end

    localparam[9:0] sprite_x = 10'd320; 
    localparam[9:0] sprite_y = 10'd240; 
    wire[4:0] lx = x - sprite_x;
    wire[4:0] ly = y - sprite_y;
    wire in_box = (x >= sprite_x && x < sprite_x + 32) && (y >= sprite_y && y < sprite_y + 32);

    wire pixel = in_box && smiley[ly][5'd31 - lx];

    always @(posedge clk) begin
        if(reset || !dataEnable) begin
            R <= 4'b0000;
            G <= 4'b0000;
            B <= 4'b0000;
        end else begin
            if(pixel) begin
                R <= 4'b1000; 
                G <= 4'b0100;
                B <= 4'b1111;
            end else begin
                R <= 4'b0000;
                G <= 4'b0000;
                B <= 4'b0000;
            end
        end
    end

endmodule
